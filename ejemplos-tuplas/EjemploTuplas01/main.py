if __name__ == '__main__':
    # Recordamos que las tuplas son listas de datos los cuales son inmutables, es decir
    # no se puede cambiar el contenido de los mismos despues de su creacion

    # Ejemplo de iteración sobre una tupla de nombres
    nombres = ("Ana", "María", "Pedro", "Jose")

    # Mostramos por pantalla los elementos de la tupla con un ciclo for
    for nombre in nombres:
        print(f"Hola, {nombre}!")

